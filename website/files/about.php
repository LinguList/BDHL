<h2>Benchmark Databases for Historical Linguistics (BDHL)</h2>
<br>
On this side, we provide benchmark resources for quantitative applications in historical 
linguistics.
These resources can help linguists to train and test common and newly developed algorithms. 
At the moment, we offer resources for three different kind of tasks, namely
<br>
<br>
<ul>
<li>pairwise and multiple <a href="alignments.php">phonetic alignment</a> analyses,</li>
<li>automatic <a href="cognates.php">cognate detection</a>, and</li>
<li> automatic <a href="proto.php">linguistic reconstruction</a>.</li>
</ul>
<br>
The resources have been taken from many different sources and were edited by our team in such a way that they 
are easy to parse and easy to convert to different formats.
If you have further questions regarding the data, please visit our <a href="faq.php">FAQ page</a>. In case these questions still remain unanswered, you will find information on how to contact us on the <a href="contact.php">contact page</a>.
If you want to download the data directly, please visit our <a href="download.php">download page</a>. 
