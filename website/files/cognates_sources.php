<h3><a style="color:black" name="SIN">Chinese Dialects (SIN)</a></h3>
<br><br>
Data was taken from the <i>Xiàndài Hànyǔ Fāngyán Yīnkù</i> (Phonetic database of Chinese dialects,
<a href="http://lingulist.de/evobib/evobib.php?key=Hou2004">Hóu (2004)</a>).
The original data consists of sound recordings and phonetic transcriptions for 180 concepts translated into 40 Chinese dialect varieties.
Here, we took but a small subset of 15 languages as gold standard for automatic cognate detection.
<br><br><ul>
  <li><b>Original data:</b> <a href="http://lingulist.de/evobib/evobib.php?key=Hou2004">Hóu (2004)</a></li>
  <li><b>Prepared by:</b> Johann-Mattis List</li>
  <li><b>Download:</b> <a href="cognates/qlc/SIN.qlc">SIN.qlc</a></li></ul> <br><br>
<h3><a style="color:black" name="TOB">Indo-European Languages (PIE, GER, ROM, SLV)</a></h3>
<br><br>
These four datasets on Indo-European languages were all taken from the
<a href="http://starling.rinet.ru/babel.php?lan=en">Tower of Babel</a> project. Apart from the Germanic subset, the data cannot be
directly browsed on the website, but it is provided along with the STARLING software package. 
The original data compiled by Sergey Nicholaev contained lexicostatistical wordlists of more than 90 different Indo-European languages.
Since only orthographic transcriptions were given, only a small subset of 19 Indo-European languages 
was prepared for which reliable
phonetic transcriptions could be used. The data can be further sub-divided into three smaller subsets for Slavic, Germanic, and Romance languages.
<br><br><ul>
  <li><b>Original data:</b> <a href="http://starling.rinet.ru/babel.php?lan=en">Tower of Babel</a>
  (<a href="http://lingulist.de/evobib/evobib.php?key=Starostin2008">Starostin 2008</a>)
  </li>
  <li><b>Compiled by:</b> Sergey Nicholaev</li>
  <li><b>Prepared by:</b> Johann-Mattis List</li>
  <li><b>Download:</b>
<a href="cognates/qlc/GER.qlc">GER.qlc</a>,
