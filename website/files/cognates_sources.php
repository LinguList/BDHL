<h3><a style="color:black" name="IDS">Romance and Germanic Languages (IDS)</a></h3>
<br><br>
This 
gold standard consists of 550 items translated into four languages (German,
English, Dutch, and French) which were taken from the <a href="http://lingweb.eva.mpg.de/ids/">Intercontinental Dictionary Series</a>. 
The
orthographic entries in the original were converted into IPA transcriptions, relying on one
dictionary source for each language in order to maintain consistency. Cognate judgments were applied
manually by consulting the respective literature (such as, among others, 
<a href="http://lingulist.de/evobib/evobib.php?key=Kluge2002">Kluge 2002</a>, 
<a href="http://lingulist.de/evobib/evobib.php?key=Meyer-Luebke1911">Meyer-Lübke 1908</a>, and
<a href="http://lingulist.de/evobib/evobib.php?key=Wodtko2008">Wodtko 2008</a>).
<br><br><ul>
  <li><b>Original data:</b> <a href="http://lingweb.eva.mpg.de/ids/">Intercontinental Dictionary Series</a>
  (<a href="http://lingulist.de/evobib/evobib.php?key=Key2007">Key & Comrie 2007</a>)
  </li>
  <li><b>Prepared by:</b> Johann-Mattis List</li>
  <li><b>Download:</b> <a href="cognates/qlc/IDS.qlc">IDS.qlc</a></li></ul> <br><br>

