<h3><a style="color:black" name="PAN">Austronesian Languages (PAN)</a></h3>
<br><br>
Data stems from the <a href="http://language.psy.auckland.ac.nz/austronesian/">Austronesian Basic Vocabulary Database</a> 
and was kindly provided by Simon Greenhill. Since ABVD data is not based on IPA transcriptions, Simon Greenhill was so friendly 
to provide parts of the data which were all compiled by Robert Blust and could thus in a rather straightforward way be automatically converted to IPA.
<br><br><ul>
  <li><b>Original data:</b> 
  <a href="http://language.psy.auckland.ac.nz/austronesian/">Austronesian Basic Vocabulary Database</a>
  (<a href="http://lingulist.de/evobib/evobib.php?key=Greenhill2008">Greenhill et al. 2008</a>)
  </li>
  <li><b>Prepared by:</b>Johann-Mattis List</li>
