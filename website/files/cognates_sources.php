<h3><a style="color:black" name="BAI">Bai Dialects (BAI)</a></h3>
<br><br>
Dataset contains cognate judgments for 9 different Bai dialect varieties, originally published as part of 
<a href="http://lingulist.de/evobib/evobib.php?key=Wang2006">Wang (2006)</a>, and now kindly provided in digital form by  
Wang Feng. 
<br><br><ul>
  <li><b>Original data:</b> <a href="http://lingulist.de/evobib/evobib.php?key=Wang2006">Wang (2006)</a> </li>
  <li><b>Compiled by:</b> Wang Feng</li>
  <li><b>Prepared by:</b> Johann-Mattis List</li>
  <li><b>Download:</b> <a href="cognates/qlc/BAI.qlc">BAI.qcl</a></li></ul> <br><br>
<h3><a style="color:black" name="IEL">Indo-European Languages (IEL)</a></h3>
<br><br>
Original data stems from the <a href="http://ielex.mpi.nl/">Indo-European lexical cognacy database</a> and was kindly provided by Michael Dunn. The phonetic entries were automatically cleaned (removing of brackets and replacement of very narrow phonetic transcriptions by broader ones) and tokenized. 
<br><br><ul>
  <li><b>Original data:</b> <a href="http://ielex.mpi.nl/">Indo-European lexical cognacy database</a>  
  (<a href="http://lingulist.de/evobib/evobib.php?key=Dunn2012">Dunn 2012</a>)
  </li>
  <li><b>Prepared by:</b> Johann-Mattis List</li>
  <li><b>Download:</b> <a href="cognates/qlc/IEL.qlc">IEL.qlc</a></li></ul> <br><br>
<h3><a style="color:black" name="JAP">Japanese Dialects (JAP)</a></h3>
<br><br>
Data was taken from <a href="http://lingulist.de/evobib/evobib.php?key=Shiro1973">Shirō (1973)</a>. 
The data was digitized by three students from Heinrich Heine University Düsseldorf during a
course on historical linguistics. Here, it is given in a form in which the original phonetic transcriptions were slightly modified in order
to be in concordance with IPA standard.
<br><br><ul>
  <li><b>Original data:</b> <a href="http://lingulist.de/evobib/evobib.php?key=Shiro1973">Shirō (1973)</a></li>
  <li><b>Digitized by:</b> Mathias Dickmanns, Sayako Maike Oetzel, and Karl Vogt</li>
  <li><b>Prepared by:</b> Johann-Mattis List</li>
  <li><b>Download:</b> <a href="cognates/qlc/JAP.qlc">JAP.qlc</a></li></ul> <br><br>
