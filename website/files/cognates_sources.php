<h3><a style="color:black" name="KSL">Various Languages (KSL)</a></h3>
<br><br>
This data was taken from 
<a href="http://lingulist.de/evobib/evobib.php?key=Kessler2001">Kessler (2001)</a>. 
The original data was available in XML format. Apart from the reordering of the data in order to conform our
specific CSV format, no further modifications were applied to the data. The dataset is specifically useful to test
to which degree a given cognate detection method can distinguish between related and unrelated languages, since it does 
not consist of a single language family.
<br><br><ul>
  <li><b>Original data:</b> <a href="http://lingulist.de/evobib/evobib.php?key=Kessler2001">Kessler (2001)</a></li>
